def build_package(schools_list, province, year=2025):
    """构建数据包"""
    schools = []
    for name, major, direction, base_rank, tuition, note in schools_list:
        schools.append({
            "name": name,
            "major": major,
            "rank_2025": base_rank,
            "tuition": tuition,
            "direction": direction,
            "color_limit": False,
            "note": note,
        })
    return schools


def main():
    os.makedirs(OUT_DIR, exist_ok=True)

    packages = [
        ("河北", HEBEI_SCHOOLS),
        ("山东", SHANDONG_SCHOOLS),
        ("广东", GUANGDONG_SCHOOLS),
        ("河南", HENAN_SCHOOLS),
    ]

    for province, schools_list in packages:
        schools = build_package(schools_list, province)
        filepath = os.path.join(OUT_DIR, f"{province}_2025.json")
        with open(filepath, "w", encoding="utf-8") as f:
            json.dump(schools, f, ensure_ascii=False, indent=2)

        # 统计
        dirs = {}
        for s in schools:
            d = s["direction"]
            dirs[d] = dirs.get(d, 0) + 1

        print(f"  OK {province}_2025.json: {len(schools)} records")
        for d, c in sorted(dirs.items()):
            print(f"       {d}: {c}")

    print(f"\nAll packages saved to {OUT_DIR}")


if __name__ == "__main__":
    main()
